// ---------------------------------------------------------------------------
// New-shape report renderer.
// ---------------------------------------------------------------------------

function NewReportTemplate({
	data,
	variant: v,
	reportId,
	generatedAt,
	reportUrl,
	pdfUrl,
	evalsUrl
}: ReportTemplateProps) {
	const sections = data.sections;
	const charts = data.charts;

	if (!sections) {
		return (
			<p {...cx(v, 'text-gray-500 dark:text-neutral-400', { color: '#6b7280' })}>
				This report is missing its rendered sections.
			</p>
		);
	}

	return (
		<>
			<ReportHeader v={v} generatedAt={generatedAt} />
			{v === 'ui' && <ExportPdfButton reportId={reportId} />}

			{/* Executive Summary */}
			{sections.executiveSummary?.text &&
				sectionWrap(
					v,
					<>
						{sectionTitle(v, 'Executive Summary')}
						<div {...cx(v, 'mt-4 space-y-3', { marginTop: '12px' })}>
							{renderParagraphs(v, sections.executiveSummary.text, 'summary')}
						</div>
					</>
				)}

			{/* Market Snapshot, with pie chart floated left */}
			{sections.marketSnapshot?.text &&
				sectionWrap(
					v,
					<>
						{sectionTitle(v, 'Market Snapshot')}
						<MarketSnapshotPie v={v} charts={charts} />
						<div {...cx(v, 'mt-2 space-y-3', { marginTop: '8px' })}>
							{renderParagraphs(v, sections.marketSnapshot.text)}
						</div>
						<ClearFloat />
					</>
				)}

			{/* Developer Signals */}
			{sections.developerSignals?.text &&
				sectionWrap(
					v,
					<>
						{sectionTitle(v, 'Developer Signals')}
						<div {...cx(v, 'mt-4 space-y-3', { marginTop: '12px' })}>
							{renderParagraphs(v, sections.developerSignals.text)}
						</div>
					</>
				)}

			{/* Centrality line chart, full width */}
			<CentralityLineChart v={v} charts={charts} />

			{/* Signal Interpretation */}
			{sections.signalInterpretation &&
				sectionWrap(
					v,
					<>
						{sectionTitle(v, 'Signal Interpretation')}
						{sections.signalInterpretation.text && (
							<div {...cx(v, 'mt-4 mb-4 space-y-3', { marginTop: '12px', marginBottom: '16px' })}>
								{renderParagraphs(v, sections.signalInterpretation.text)}
							</div>
						)}
						{sections.signalInterpretation.interpretations?.map((interp, i) => (
							<InterpretationCard key={i} v={v} interpretation={interp} />
						))}
					</>
				)}

			{/* Supporting Resources */}
			{sections.supportingResources?.resources?.length > 0 &&
				sectionWrap(
					v,
					<>
						{sectionTitle(v, 'Supporting Resources')}
						<SupportingResourcesSection v={v} resources={sections.supportingResources.resources} />
					</>
				)}

			{/* Email-only CTAs */}
			{v === 'email' && reportUrl && (
				<div style={{ padding: '16px 24px 28px', textAlign: 'center' }}>
					<a
						href={reportUrl}
						style={{
							display: 'inline-block',
							background: '#4f46e5',
							color: 'white',
							padding: '12px 24px',
							borderRadius: '8px',
							textDecoration: 'none',
							fontWeight: 600,
							marginRight: '8px'
						}}
					>
						View Full Report
					</a>
					{pdfUrl && (
						<a
							href={pdfUrl}
							style={{
								display: 'inline-block',
								background: '#f3f4f6',
								color: '#374151',
								padding: '12px 24px',
								borderRadius: '8px',
								textDecoration: 'none',
								fontWeight: 600,
								marginRight: '8px'
							}}
						>
							Download PDF
						</a>
					)}
					{evalsUrl && (
						<a
							href={evalsUrl}
							style={{
								display: 'inline-block',
								background: '#f3f4f6',
								color: '#374151',
								padding: '12px 24px',
								borderRadius: '8px',
								textDecoration: 'none',
								fontWeight: 600
							}}
						>
							View Scores
						</a>
					)}
				</div>
			)}
		</>
	);
}

// ---------------------------------------------------------------------------
// Top-level dispatcher.
// ---------------------------------------------------------------------------

export default function ReportTemplate(props: ReportTemplateProps) {
	if (isLegacyReportData(props.data)) {
		return <LegacyReportTemplate {...props} />;
	}
	return <NewReportTemplate {...props} />;
}
